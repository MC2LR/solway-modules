export default {
    props: ['data'],
    data () {
        return {}
    },
    mounted () {
        this.$emit('hasDevice', {
            d1: true,
            d2: true,
            d3: true,
            d4: true,
            d5: true,
            d6: true
        })
        this.timer1 = setTimeout(() => {
            this.$el.className += ' loaded'
            this.$refs.svg.style.transform = 'translateY(0)'
        }, 300)
        this.timer2 = setTimeout(this.getXY, 1000)
        window.addEventListener('resize', this.getXY)
    },
    methods: {
        async getXY (event) {
            const {position1, position2, position3, position4, position5, position6} = this.$refs
            const positions = {
                p1: this.getLeftTop(position1.getBoundingClientRect()),
                p2: this.getLeftTop(position2.getBoundingClientRect()),
                p3: this.getLeftTop(position3.getBoundingClientRect()),
                p4: this.getLeftTop(position4.getBoundingClientRect()),
                p5: this.getLeftTop(position5.getBoundingClientRect()),
                p6: this.getLeftTop(position6.getBoundingClientRect())
            }
            if (event) this.$emit('loadedAndRander', positions)
            const {p1, p2, p3, p4, p5, p6} = positions
            this.$emit('loadedAndRander', {p1})
            await this.$timeout(200)
            this.$emit('loadedAndRander', {p2})
            await this.$timeout(200)
            this.$emit('loadedAndRander', {p3})
            await this.$timeout(200)
            this.$emit('loadedAndRander', {p4})
            await this.$timeout(200)
            this.$emit('loadedAndRander', {p5})
            await this.$timeout(200)
            this.$emit('loadedAndRander', {p6})
        },
        getLeftTop (o) {
            return {
                left: o.left,
                top: o.top + o.width / 2
            }
        }
    },
    beforeDestroy () {
        clearTimeout(this.timer1)
        clearTimeout(this.timer2)
        window.removeEventListener('resize', this.getXY)
    },
    components: {
        SectionE: () => import('../section/SectionE'),
        SectionF: () => import('../section/SectionF')
    }
}
